# Este es un programa en R para calcular la desviación estándar de una lista de números.

# Crear una lista de números
numeros <- c(10, 15, 20, 25, 30)

# Calcular la desviación estándar
desviacion_estandar <- sd(numeros)

# Mostrar el resultado
cat("La desviación estándar de los números es:", desviacion_estandar, "\n")
